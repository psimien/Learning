def add_num(x, y):
    return x + y


def sub_num(x, y):
    return x - y


def multi_num(x, y):
    return x * y


def devide_num(x, y):
    return x / y


def exp_num(x, y):
    return x ^ y


def mod_num(x, y):
    return x % y


